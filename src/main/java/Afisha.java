public class Afisha {

    private int id;
    private String name;
    private String genre;
    private String url;

    public Afisha(int id, String name, String genre, String url) {
        this.id = id;
        this.name = name;
        this.genre = genre;
        this.url = url;
    }

    public int getId() {
        return id;
    }
}